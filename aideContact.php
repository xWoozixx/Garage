    <h2>Comment rechercher un véhicule ?</h2>
    <p>Pour rechercher un véhicule, vous pouvez utiliser la fonction de recherche sur notre site. Suivez ces étapes :</p>
    <ol>
        <li>Rendez-vous sur la page d'accueil du site.</li>
        <li>Localisez la zone de recherche, généralement un champ de texte.</li>
        <li>Saisissez l'immatriculation du véhicule que vous souhaitez rechercher.</li>
        <li>Cliquez sur le bouton "Rechercher" pour lancer la recherche.</li>
    </ol>

    <h2>Comment ajouter un véhicule à votre garage ?</h2>
    <p>Pour ajouter un véhicule à votre garage personnel, veuillez suivre les étapes ci-dessous :</p>
    <ol>
        <li>Connectez-vous à votre compte utilisateur sur notre site.</li>
        <li>Naviguez vers la page de gestion de votre garage.</li>
        <li>Cliquez sur le bouton "Ajouter un véhicule" ou une option similaire.</li>
        <li>Renseignez les informations requises, telles que l'immatriculation, la marque, le modèle, etc.</li>
        <li>Cliquez sur le bouton "Ajouter" pour enregistrer le véhicule dans votre garage.</li>
    </ol>

